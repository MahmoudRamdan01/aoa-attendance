create or replace function get_my_context_v1()
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  v_admin app_admins%rowtype;
  v_account employee_accounts%rowtype;
  v_employee jsonb;
  v_location jsonb;
  v_role text := 'employee';
  v_email text;
begin
  select email into v_email from auth.users where id = auth.uid();
  select * into v_admin from app_admins where user_id = auth.uid();
  select * into v_account from employee_accounts where user_id = auth.uid() and active;

  if v_admin.user_id is not null then
    v_role := v_admin.role;
  elsif v_account.user_id is not null then
    v_role := v_account.role;
  end if;

  if v_account.employee_id is not null then
    select to_jsonb(e.*) into v_employee from employees e where e.id = v_account.employee_id;
  end if;

  select to_jsonb(cl.*) into v_location
  from company_locations cl
  where cl.active
  order by cl.id
  limit 1;

  return jsonb_build_object(
    'role', v_role,
    'admin_name', coalesce(v_admin.name, v_email),
    'employee', v_employee,
    'location', v_location,
    'today', (now() at time zone 'Africa/Cairo')::date
  );
end $$;

create or replace function get_daily_qr_v1()
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  v_date date := (now() at time zone 'Africa/Cairo')::date;
  v_code text;
begin
