create table if not exists missing_checkout_counters (
  employee_id bigint not null references employees(id) on delete cascade,
  work_month text not null,
  warning_count int not null default 0,
  deduction_count int not null default 0,
  primary key(employee_id, work_month)
);

create or replace function mark_missing_checkouts_v1(p_date date)
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  r record;
  v_month text := to_char(p_date,'YYYY-MM');
  v_count int;
  v_user uuid;
begin
  if not is_hr() then return jsonb_build_object('error','hr_only','message','للإدارة فقط.'); end if;
  for r in
    select * from attendance
    where work_date=p_date and check_in is not null and check_out is null and status in ('present','late')
  loop
    insert into missing_checkout_counters(employee_id,work_month,warning_count)
    values (r.employee_id,v_month,1)
    on conflict (employee_id,work_month)
    do update set warning_count = missing_checkout_counters.warning_count + 1
    returning warning_count into v_count;

    select user_id into v_user from employee_accounts where employee_id = r.employee_id;

    if v_count <= 2 then
      perform notify_user(v_user,'تنبيه عدم تسجيل انصراف','دي المرة رقم ' || v_count::text || ' في الشهر.');
    else
      update attendance
      set deduction_days = coalesce(deduction_days,0) + 0.25,
          note = coalesce(note || ' · ','') || 'خصم ربع يوم لعدم تسجيل الانصراف'
      where id = r.id;
      update missing_checkout_counters
      set deduction_count = deduction_count + 1
      where employee_id = r.employee_id and work_month = v_month;
      perform notify_user(v_user,'خصم عدم تسجيل انصراف','تم تطبيق خصم ربع يوم بعد تكرار عدم تسجيل الانصراف.');
    end if;
  end loop;
  return jsonb_build_object('ok',true);
end $$;

create or replace function owner_list_employee_accounts_v1()
returns table(
  employee_id bigint,
  employee_name text,
  user_id uuid,
  email text,
  role text,
  active boolean
)
language sql security definer set search_path=public as $$
  select
    e.id as employee_id,
    e.name as employee_name,
    ea.user_id,
    u.email,
    ea.role,
    coalesce(ea.active,false) as active
  from employees e
  left join employee_accounts ea on ea.employee_id = e.id
  left join auth.users u on u.id = ea.user_id
  where e.active
  order by e.id;
$$;

create or replace function owner_link_employee_account_v1(
  p_employee_id bigint,
  p_email text,
  p_role text default 'employee'
)
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  v_user uuid;
begin
  if not is_owner() then
    return jsonb_build_object('error','owner_only','message','Owner فقط يقدر يربط حسابات الموظفين.');
  end if;

  if p_role not in ('employee','hr','owner') then
    return jsonb_build_object('error','bad_role','message','الدور غير صحيح.');
  end if;

  if not exists (select 1 from employees where id=p_employee_id and active) then
    return jsonb_build_object('error','employee_not_found','message','الموظف غير موجود أو غير نشط.');
  end if;

  select id into v_user
  from auth.users
  where lower(email) = lower(trim(p_email))
  limit 1;

  if v_user is null then
    return jsonb_build_object('error','auth_user_missing','message','اعمل حساب Auth بالإيميل ده الأول من Supabase Authentication.');
  end if;

  delete from employee_accounts
  where employee_id = p_employee_id and user_id <> v_user;

  insert into employee_accounts(user_id,employee_id,role,active)
  values (v_user,p_employee_id,p_role,true)
  on conflict (user_id) do update
    set employee_id=excluded.employee_id,
        role=excluded.role,
        active=true;

  insert into audit_log(actor,action,entity,entity_id,details)
  values (auth.uid(),'link_employee_account','employee_accounts',p_employee_id::text,jsonb_build_object('email',p_email,'role',p_role));

  return jsonb_build_object('ok',true);
end $$;

