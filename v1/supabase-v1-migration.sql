create or replace function geo_distance_meters(lat1 numeric, lng1 numeric, lat2 numeric, lng2 numeric)
returns numeric language sql immutable as $$
  select 6371000 * 2 * asin(sqrt(
    power(sin(radians(($3 - $1)::double precision) / 2), 2) +
    cos(radians($1::double precision)) * cos(radians($3::double precision)) *
    power(sin(radians(($4 - $2)::double precision) / 2), 2)
  ))::numeric;
$$;

create or replace function current_employee_id()
returns bigint language sql stable security definer set search_path=public as $$
  select employee_id from employee_accounts where user_id = auth.uid() and active limit 1;
$$;

