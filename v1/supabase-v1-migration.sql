create or replace function request_permission_v1(p_date date, p_hours_requested numeric, p_reason text)
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  v_emp bigint := current_employee_id();
  v_limit int;
  v_count int;
  v_today date := (now() at time zone 'Africa/Cairo')::date;
begin
  if v_emp is null then return jsonb_build_object('error','no_employee','message','الحساب غير مربوط بموظف.'); end if;
  if p_hours_requested not in (1,2) then return jsonb_build_object('error','bad_hours','message','مدة الإذن ساعة أو ساعتين.'); end if;
  if p_date < v_today then return jsonb_build_object('error','past_date','message','لا يمكن طلب إذن لتاريخ سابق.'); end if;
  if coalesce(trim(p_reason),'') = '' then return jsonb_build_object('error','reason_required','message','لازم تكتب سبب الإذن.'); end if;

  select coalesce((select (value#>>'{}')::int from settings where key='monthly_permission_max'),3) into v_limit;
  select count(*) into v_count from permissions
  where employee_id=v_emp and status in ('pending','approved')
    and date_trunc('month',perm_date)=date_trunc('month',p_date);
  if v_count >= v_limit then return jsonb_build_object('error','limit','message','وصلت الحد الأقصى للأذونات الشهرية.'); end if;

  if exists (
    select 1 from permissions
    where employee_id=v_emp and status in ('pending','approved')
      and perm_date in (p_date - interval '1 day', p_date + interval '1 day')
  ) then
    return jsonb_build_object('error','consecutive','message','غير مسموح بإذنين في يومين متتاليين.');
  end if;

  insert into permissions(employee_id,perm_date,hours,hours_requested,reason,status,requested_by)
  values (v_emp,p_date,p_hours_requested,p_hours_requested,p_reason,'pending',auth.uid());

  perform notify_admins('طلب إذن جديد','يوجد طلب إذن جديد يحتاج موافقة.');
  return jsonb_build_object('ok',true,'message','تم إرسال طلب الإذن.');
end $$;

create or replace function decide_permission_v1(p_id bigint, p_approve boolean, p_hours_approved numeric, p_note text default null)
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  v permissions%rowtype;
  v_user uuid;
begin
  if not is_hr() then return jsonb_build_object('error','hr_only','message','للإدارة فقط.'); end if;
  select * into v from permissions where id=p_id;
  if not found then return jsonb_build_object('error','not_found','message','الطلب غير موجود.'); end if;
  if p_approve and p_hours_approved not in (1,2) then return jsonb_build_object('error','bad_hours','message','مدة الموافقة ساعة أو ساعتين.'); end if;

  update permissions
  set status = case when p_approve then 'approved' else 'rejected' end,
      hours_approved = case when p_approve then p_hours_approved else null end,
      decision_note = p_note,
      decided_at = now(),
      decided_by = auth.uid()
  where id=p_id;

  select user_id into v_user from employee_accounts where employee_id = v.employee_id;
  perform notify_user(v_user, case when p_approve then 'تمت الموافقة على الإذن' else 'تم رفض الإذن' end,
    case when p_approve then 'المدة المعتمدة: ' || p_hours_approved::text || ' ساعة.' else coalesce(p_note,'تم رفض الطلب.') end);

  return jsonb_build_object('ok',true);
end $$;

create or replace function leave_work_days_v1(p_from date, p_to date)
returns int language sql stable as $$
  select count(*)::int from generate_series(p_from,p_to,interval '1 day') d
  where extract(dow from d)::int <> 5
    and not exists (
      select 1 from official_holidays h
      where h.holiday_date = d::date
    );
$$;

create or replace function request_leave_v1(p_from date, p_to date, p_cover bigint, p_reason text)
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  v_emp bigint := current_employee_id();
  v_days int;
  v_used numeric;
  v_today date := (now() at time zone 'Africa/Cairo')::date;
  v_balance numeric;
begin
  if v_emp is null then return jsonb_build_object('error','no_employee','message','الحساب غير مربوط بموظف.'); end if;
  if p_from <= v_today then return jsonb_build_object('error','too_late','message','الأجازة لازم قبلها بيوم على الأقل.'); end if;
  if p_to < p_from then return jsonb_build_object('error','bad_dates','message','تاريخ النهاية غير صحيح.'); end if;
  if p_cover is null or p_cover = v_emp then return jsonb_build_object('error','cover','message','لازم تختار Cover مختلف.'); end if;
  if coalesce(trim(p_reason),'') = '' then return jsonb_build_object('error','reason_required','message','لازم تكتب سبب الأجازة.'); end if;

  v_days := leave_work_days_v1(p_from,p_to);
  if v_days <= 0 then return jsonb_build_object('error','bad_days','message','الفترة لا تحتوي أيام عمل.'); end if;
  if v_days > 2 then return jsonb_build_object('error','max_days','message','الحد الأقصى يومين شهريًا.'); end if;

  if exists (
    select 1 from leave_requests
    where employee_id=v_emp and status in ('pending','approved')
      and (from_date in (p_from - interval '1 day', p_to + interval '1 day')
        or to_date in (p_from - interval '1 day', p_to + interval '1 day'))
  ) then
    return jsonb_build_object('error','consecutive','message','غير مسموح بأجازات متتالية.');
  end if;

  select coalesce(sum(days),0) into v_used from leave_requests
  where employee_id=v_emp and status in ('pending','approved')
    and date_trunc('month',from_date)=date_trunc('month',p_from);
  if v_used + v_days > 2 then return jsonb_build_object('error','monthly_limit','message','تجاوزت حد الأجازات الشهري.'); end if;

  select leave_balance into v_balance from employees where id=v_emp;
  if coalesce(v_balance,0) < v_days then return jsonb_build_object('error','balance','message','رصيد الأجازات غير كافي.'); end if;

  insert into leave_requests(employee_id,from_date,to_date,days,cover_employee_id,reason,status,requested_by)
  values (v_emp,p_from,p_to,v_days,p_cover,p_reason,'pending',auth.uid());

  perform notify_admins('طلب أجازة جديد','يوجد طلب أجازة جديد يحتاج موافقة.');
  return jsonb_build_object('ok',true,'message','تم إرسال طلب الأجازة.');
end $$;

create or replace function decide_leave_v1(p_id bigint, p_approve boolean, p_note text default null)
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  v leave_requests%rowtype;
  v_user uuid;
  d date;
begin
  if not is_hr() then return jsonb_build_object('error','hr_only','message','للإدارة فقط.'); end if;
  select * into v from leave_requests where id=p_id;
  if not found then return jsonb_build_object('error','not_found','message','الطلب غير موجود.'); end if;

  update leave_requests
  set status = case when p_approve then 'approved' else 'rejected' end,
      decision_note = p_note,
      decided_at = now(),
      decided_by = auth.uid()
  where id=p_id;

  select user_id into v_user from employee_accounts where employee_id = v.employee_id;

  if p_approve then
    update employees set leave_balance = greatest(0,coalesce(leave_balance,0)-v.days) where id=v.employee_id;
    for d in select generate_series(v.from_date,v.to_date,interval '1 day')::date loop
      if extract(dow from d)::int <> 5 then
        insert into attendance(employee_id,work_date,status,late_minutes,deduction_days,note,source,approved,recorded_by)
        values (v.employee_id,d,'leave',0,0,'أجازة معتمدة · Cover: ' || v.cover_employee_id,'hr',true,auth.uid())
        on conflict (employee_id,work_date) do update
          set status='leave', check_in=null, check_out=null, late_minutes=0, deduction_days=0,
              note=excluded.note, source='hr', approved=true, recorded_by=auth.uid();
      end if;
    end loop;
  end if;

  perform notify_user(v_user, case when p_approve then 'تمت الموافقة على الأجازة' else 'تم رفض الأجازة' end,
    coalesce(p_note, case when p_approve then 'تم اعتماد طلب الأجازة.' else 'تم رفض طلب الأجازة.' end));

  return jsonb_build_object('ok',true);
end $$;

