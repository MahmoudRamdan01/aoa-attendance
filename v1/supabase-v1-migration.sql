create extension if not exists pgcrypto;

-- ---------------------------------------------------------------------
-- Account mapping: every employee logs in with Supabase Auth.
-- Owner/HR remain in app_admins; employees are mapped here.
-- ---------------------------------------------------------------------
create table if not exists employee_accounts (
  user_id uuid primary key references auth.users(id) on delete cascade,
  employee_id bigint unique references employees(id) on delete cascade,
  role text not null default 'employee' check (role in ('employee','hr','owner')),
  active boolean not null default true,
  created_at timestamptz default now()
);

alter table employee_accounts enable row level security;

drop policy if exists employee_accounts_self_select on employee_accounts;
create policy employee_accounts_self_select
on employee_accounts for select to authenticated
using (user_id = auth.uid() or is_hr());

drop policy if exists employee_accounts_owner_all on employee_accounts;
create policy employee_accounts_owner_all
on employee_accounts for all to authenticated
using (is_owner()) with check (is_owner());

