create or replace function ensure_daily_qr(p_date date)
returns text language plpgsql security definer set search_path=public as $$
declare
  v_code text;
begin
  select code into v_code from daily_qr_codes where work_date = p_date;
  if v_code is null then
    v_code := upper(substr(md5(p_date::text || ':' || gen_random_uuid()::text), 1, 6));
    insert into daily_qr_codes(work_date, code, created_by)
    values (p_date, v_code, auth.uid())
    on conflict (work_date) do nothing;
    select code into v_code from daily_qr_codes where work_date = p_date;
  end if;
  return v_code;
end $$;

