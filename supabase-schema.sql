create table if not exists permissions (
  id bigint generated by default as identity primary key,
  employee_id bigint not null references employees(id) on delete cascade,
  perm_date date not null,
  hours numeric(3,1),               -- 1 أو 2 (عدد ساعات الانصراف المبكر)
  start_time time, end_time time,   -- اختياري: وقت محدد
  reason text,
  status text not null default 'pending' check (status in ('pending','approved','rejected')),
  decided_by uuid references auth.users(id), decided_at timestamptz,
  created_at timestamptz default now()
);
create index if not exists idx_perm_emp_date on permissions(employee_id, perm_date);

-- ---- طلب إذن (الموظف عبر الكيوسك — anon) ----
create or replace function request_permission(
  p_emp bigint, p_pin text, p_date date,
  p_hours numeric default null, p_start time default null, p_end time default null, p_reason text default null)
returns jsonb language plpgsql security definer set search_path=public, extensions as $$
declare v_max int; v_cnt int; v_name text;
begin
  if not exists (select 1 from employees where id=p_emp and active and pin_hash=crypt(p_pin,pin_hash)) then
    return jsonb_build_object('error','bad_pin','message','PIN غلط.');
  end if;
  select (value#>>'{}')::int into v_max from settings where key='monthly_permission_max';

  -- الحد الأقصى الشهري (معلّق + معتمد)
  select count(*) into v_cnt from permissions
   where employee_id=p_emp and status in ('pending','approved')
     and date_trunc('month',perm_date)=date_trunc('month',p_date);
  if v_cnt >= coalesce(v_max,3) then
    return jsonb_build_object('error','max','message','وصلت الحد الأقصى للإذن الشهر ('||coalesce(v_max,3)||' مرات).');
  end if;

  -- مش في يومين متتاليين
  if exists (select 1 from permissions where employee_id=p_emp and status in ('pending','approved')
             and perm_date in (p_date - 1, p_date + 1)) then
    return jsonb_build_object('error','consecutive','message','مينفعش إذن في يومين ورا بعض.');
  end if;

  -- مش نفس اليوم مرتين
  if exists (select 1 from permissions where employee_id=p_emp and perm_date=p_date and status in ('pending','approved')) then
    return jsonb_build_object('error','dup','message','فيه طلب إذن لنفس اليوم موجود بالفعل.');
  end if;

  insert into permissions(employee_id,perm_date,hours,start_time,end_time,reason)
  values (p_emp,p_date,p_hours,p_start,p_end,p_reason);
  insert into audit_log(actor,action,entity,entity_id,details)
  values (null,'permission_request','permissions',p_emp::text,jsonb_build_object('date',p_date,'hours',p_hours));
  select name into v_name from employees where id=p_emp;
  return jsonb_build_object('ok',true,'name',v_name,'message','تم إرسال الطلب — في انتظار موافقة المدير.');
end $$;
grant execute on function request_permission(bigint,text,date,numeric,time,time,text) to anon, authenticated;

-- ---- قرار المدير (HR/Owner) ----
create or replace function decide_permission(p_id bigint, p_approve boolean)
returns jsonb language plpgsql security definer set search_path=public as $$
begin
  if not is_hr() then return jsonb_build_object('error','forbidden','message','للإدارة فقط.'); end if;
  update permissions set status = case when p_approve then 'approved' else 'rejected' end,
    decided_by=auth.uid(), decided_at=now() where id=p_id;
  insert into audit_log(actor,action,entity,entity_id,details)
  values (auth.uid(),'permission_decide','permissions',p_id::text,jsonb_build_object('approve',p_approve));
  return jsonb_build_object('ok',true);
end $$;
grant execute on function decide_permission(bigint,boolean) to authenticated;

