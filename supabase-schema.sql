-- =====================================================================
--  7) do_checkin — بيسجّل الخصم ككسر يوم (مفيش مرتب هنا خالص)
-- =====================================================================
create or replace function do_checkin(p_emp bigint, p_pin text)
returns jsonb language plpgsql security definer set search_path=public, extensions as $$
declare
  s_from time; s_to time; s_start time; s_absent time;
  v_now timestamp; v_date date; v_time time; v_min int; v_late int;
  v_tier jsonb; v_tiers jsonb; v_status text:='pending'; v_label text:=''; v_cut numeric:=0; v_name text;
begin
  if not exists (select 1 from employees where id=p_emp and active
                 and pin_hash=crypt(p_pin,pin_hash)) then
    return jsonb_build_object('error','bad_pin','message','PIN غلط.');
  end if;
  select (value#>>'{}')::time into s_from   from settings where key='checkin_from';
  select (value#>>'{}')::time into s_to     from settings where key='checkin_to';
  select (value#>>'{}')::time into s_start  from settings where key='work_start';
  select (value#>>'{}')::time into s_absent from settings where key='absent_after';
  select value into v_tiers from settings where key='late_tiers';

  v_now := now() at time zone 'Africa/Cairo';
  v_date := v_now::date; v_time := v_now::time;
  v_min := extract(hour from v_now)::int*60 + extract(minute from v_now)::int;

  if v_time < s_from or v_time > s_to then
    return jsonb_build_object('error','window_closed','message',
      'نافذة التسجيل ('||to_char(s_from,'HH24:MI')||'–'||to_char(s_to,'HH24:MI')||') مقفولة دلوقتي.');
  end if;
  if exists (select 1 from attendance where employee_id=p_emp and work_date=v_date) then
    return jsonb_build_object('error','already','message','سجّلت حضورك النهاردة بالفعل.');
  end if;

  v_late := greatest(0, v_min - (extract(hour from s_start)::int*60 + extract(minute from s_start)::int));
  if v_time >= s_absent then v_status:='pending'; v_label:='بعد 10:00 · موافقة المدير'; v_cut:=0;
  else
    for v_tier in select * from jsonb_array_elements(v_tiers) loop
      if v_late <= (v_tier->>'upto')::int then
        v_status:=v_tier->>'status'; v_label:=v_tier->>'label'; v_cut:=(v_tier->>'cut')::numeric; exit;
      end if;
    end loop;
  end if;

  select name into v_name from employees where id=p_emp;
  insert into attendance(employee_id,work_date,check_in,status,late_minutes,deduction_days,source,approved)
  values (p_emp,v_date,v_time,v_status,v_late,v_cut,'kiosk',false);
  insert into audit_log(actor,action,entity,entity_id,details)
  values (null,'checkin','attendance',p_emp::text,jsonb_build_object('date',v_date,'time',v_time,'status',v_status));

  return jsonb_build_object('ok',true,'name',v_name,'status',v_status,'label',v_label,
    'time',to_char(v_time,'HH24:MI'),'lateMin',v_late,'deductionDays',v_cut);
end $$;

-- =====================================================================
--  8) سلطة الـ Owner: حالة خاصة / تصفية غياب يوم معيّن
-- =====================================================================
create or replace function owner_set_special(p_emp bigint, p_date date, p_status text, p_note text default 'حالة خاصة')
returns jsonb language plpgsql security definer set search_path=public as $$
begin
  if not is_owner() then return jsonb_build_object('error','owner_only','message','للـ Owner فقط.'); end if;
  insert into attendance(employee_id,work_date,status,deduction_days,note,source,approved,recorded_by)
  values (p_emp,p_date,p_status,0,p_note,'owner',true,auth.uid())
  on conflict (employee_id,work_date) do update
    set status=excluded.status, deduction_days=0, note=excluded.note, source='owner', approved=true, recorded_by=auth.uid();
  insert into audit_log(actor,action,entity,entity_id,details)
  values (auth.uid(),'owner_special','attendance',p_emp::text,jsonb_build_object('date',p_date,'status',p_status,'note',p_note));
  return jsonb_build_object('ok',true);
end $$;

