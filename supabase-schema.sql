-- وسّع حالات الحضور: زوّدنا مأمورية (mission) + مرضي (sick)
alter table attendance drop constraint if exists attendance_status_check;
alter table attendance add constraint attendance_status_check
  check (status in ('present','late','absent','leave','pending','mission','sick'));

-- ---- جدول طلبات الأجازة ----
create table if not exists leave_requests (
  id bigint generated by default as identity primary key,
  employee_id bigint not null references employees(id) on delete cascade,
  from_date date not null, to_date date not null,
  days int not null check (days>0),
  cover_employee_id bigint references employees(id),
  reason text,
  status text not null default 'pending' check (status in ('pending','approved','rejected')),
  decided_by uuid references auth.users(id), decided_at timestamptz,
  created_at timestamptz default now(),
  check (to_date>=from_date),
  check (cover_employee_id is null or cover_employee_id<>employee_id)
);
create index if not exists idx_leave_emp on leave_requests(employee_id);

-- ---- طلب أجازة (الموظف عبر الكيوسك) ----
create or replace function request_leave(
  p_emp bigint, p_pin text, p_from date, p_to date, p_cover bigint, p_reason text default null)
returns jsonb language plpgsql security definer set search_path=public, extensions as $$
declare v_today date; v_days int; v_max int; v_used int; v_name text;
begin
  if not exists (select 1 from employees where id=p_emp and active and pin_hash=crypt(p_pin,pin_hash)) then
    return jsonb_build_object('error','bad_pin','message','PIN غلط.');
  end if;
  if p_cover is null then return jsonb_build_object('error','no_cover','message','لازم تختار الموظف اللي هيغطّيك.'); end if;
  if p_cover = p_emp then return jsonb_build_object('error','self_cover','message','مينفعش تغطّي نفسك.'); end if;
  if p_to < p_from then return jsonb_build_object('error','range','message','تاريخ خطأ.'); end if;

  v_today := (now() at time zone 'Africa/Cairo')::date;
  -- 24 ساعة على الأقل (يبدأ من بكرة على الأقل)
  if p_from <= v_today then
    return jsonb_build_object('error','notice','message','لازم تقدّم الطلب قبلها بيوم على الأقل.');
  end if;

  -- عدد الأيام (بدون الجمعة)
  select count(*) into v_days from generate_series(p_from,p_to,interval '1 day') g where extract(dow from g)<>5;
  if v_days<1 then return jsonb_build_object('error','range','message','مفيش أيام عمل في المدى ده.'); end if;

  -- الحد الأقصى الشهري
  select (value#>>'{}')::int into v_max from settings where key='monthly_leave_max';
  select coalesce(sum(days),0) into v_used from leave_requests
    where employee_id=p_emp and status in ('pending','approved')
      and date_trunc('month',from_date)=date_trunc('month',p_from);
  if v_used + v_days > coalesce(v_max,2) then
    return jsonb_build_object('error','max','message','تعدّيت الحد الشهري للأجازات ('||coalesce(v_max,2)||' يوم).');
  end if;

  insert into leave_requests(employee_id,from_date,to_date,days,cover_employee_id,reason)
  values (p_emp,p_from,p_to,v_days,p_cover,p_reason);
  insert into audit_log(actor,action,entity,entity_id,details)
  values (null,'leave_request','leave_requests',p_emp::text,jsonb_build_object('from',p_from,'to',p_to,'days',v_days,'cover',p_cover));
  select name into v_name from employees where id=p_emp;
  return jsonb_build_object('ok',true,'name',v_name,'days',v_days,'message','تم إرسال طلب الأجازة — في انتظار الموافقة.');
end $$;
grant execute on function request_leave(bigint,text,date,date,bigint,text) to anon, authenticated;

-- ---- قرار المدير على الأجازة ----
create or replace function decide_leave(p_id bigint, p_approve boolean)
returns jsonb language plpgsql security definer set search_path=public as $$
declare v_req record; d date;
begin
  if not is_hr() then return jsonb_build_object('error','forbidden','message','للإدارة فقط.'); end if;
  select * into v_req from leave_requests where id=p_id;
  if not found then return jsonb_build_object('error','notfound'); end if;

  if p_approve then
    for d in select g::date from generate_series(v_req.from_date,v_req.to_date,interval '1 day') g where extract(dow from g)<>5 loop
      insert into attendance(employee_id,work_date,status,deduction_days,source,approved,note)
      values (v_req.employee_id,d,'leave',0,'leave',true,'أجازة معتمدة')
      on conflict (employee_id,work_date) do update set status='leave',deduction_days=0,approved=true,note='أجازة معتمدة';
    end loop;
    update employees set leave_balance = greatest(0, leave_balance - v_req.days) where id=v_req.employee_id;
  end if;

  update leave_requests set status=case when p_approve then 'approved' else 'rejected' end,
    decided_by=auth.uid(), decided_at=now() where id=p_id;
  insert into audit_log(actor,action,entity,entity_id,details)
  values (auth.uid(),'leave_decide','leave_requests',p_id::text,jsonb_build_object('approve',p_approve));
  return jsonb_build_object('ok',true);
end $$;
grant execute on function decide_leave(bigint,boolean) to authenticated;

-- ---- تصنيف يوم (Pending Absence → أجازة/غياب/مأمورية/مرضي) ----
create or replace function set_day_status(p_emp bigint, p_date date, p_kind text)
returns jsonb language plpgsql security definer set search_path=public as $$
declare v_old text; v_ded numeric:=0;
begin
  if not is_hr() then return jsonb_build_object('error','forbidden','message','للإدارة فقط.'); end if;
  if p_kind not in ('leave','absent','mission','sick','present') then
    return jsonb_build_object('error','bad_kind'); end if;

  select status into v_old from attendance where employee_id=p_emp and work_date=p_date;
  v_ded := case when p_kind='absent' then 1 else 0 end;

  insert into attendance(employee_id,work_date,status,deduction_days,source,approved,note)
  values (p_emp,p_date,p_kind,v_ded,'hr',true,null)
  on conflict (employee_id,work_date) do update set status=p_kind, deduction_days=v_ded, source='hr', approved=true;

  -- تعديل رصيد الأجازة (يوم واحد)
  if p_kind='leave' and (v_old is distinct from 'leave') then
    update employees set leave_balance=greatest(0,leave_balance-1) where id=p_emp;
  elsif v_old='leave' and p_kind<>'leave' then
    update employees set leave_balance=leave_balance+1 where id=p_emp;
  end if;

  insert into audit_log(actor,action,entity,entity_id,details)
  values (auth.uid(),'classify','attendance',p_emp::text,jsonb_build_object('date',p_date,'kind',p_kind));
  return jsonb_build_object('ok',true);
end $$;
grant execute on function set_day_status(bigint,date,text) to authenticated;

