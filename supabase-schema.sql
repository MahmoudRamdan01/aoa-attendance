-- ========== الجزء 2/4 — الانصراف (Check-out) ==========
-- =====================================================================
--  Air Ocean Line — Module 2: الانصراف (Check-out)
--  patch إضافي — شغّله بعد schema v3.  SQL Editor → الصق → Run
--  (نافذة الانصراف وعمود check_out موجودين أصلًا من v3)
-- =====================================================================

create or replace function do_checkout(p_emp bigint, p_pin text)
returns jsonb language plpgsql security definer set search_path=public, extensions as $$
declare
  s_from time; s_to time; s_grace time;
  v_now timestamp; v_date date; v_time time; v_rec record; v_name text; v_note text;
begin
  -- 1) PIN
  if not exists (select 1 from employees where id=p_emp and active and pin_hash=crypt(p_pin,pin_hash)) then
    return jsonb_build_object('error','bad_pin','message','PIN غلط.');
  end if;

  select (value#>>'{}')::time into s_from  from settings where key='checkout_from';
  select (value#>>'{}')::time into s_to    from settings where key='checkout_to';
  select (value#>>'{}')::time into s_grace from settings where key='checkout_grace_to';

  v_now := now() at time zone 'Africa/Cairo';
  v_date := v_now::date; v_time := v_now::time;

  -- 2) لازم يكون جوّه نافذة الانصراف
  if v_time < s_from or v_time > s_to then
    return jsonb_build_object('error','window_closed','message',
      'نافذة الانصراف ('||to_char(s_from,'HH24:MI')||'–'||to_char(s_to,'HH24:MI')||') مقفولة دلوقتي.');
  end if;

  -- 3) لازم يكون سجّل حضور الأول
  select * into v_rec from attendance where employee_id=p_emp and work_date=v_date;
  if not found then
    return jsonb_build_object('error','no_checkin','message','لازم تسجّل حضور الأول قبل الانصراف.');
  end if;
  if v_rec.check_out is not null then
    return jsonb_build_object('error','already_out','message','سجّلت انصرافك بالفعل ('||to_char(v_rec.check_out,'HH24:MI')||').');
  end if;

  -- انصراف 4:30–5:00 = عادي بدون خصم (v_note للتوضيح فقط)
  v_note := case when v_time <= s_grace then 'انصراف مبكر ضمن السماح' else null end;

  update attendance set check_out = v_time,
    note = coalesce(note, v_note)
  where employee_id=p_emp and work_date=v_date;

  insert into audit_log(actor,action,entity,entity_id,details)
  values (null,'checkout','attendance',p_emp::text, jsonb_build_object('date',v_date,'time',v_time));

  select name into v_name from employees where id=p_emp;
  return jsonb_build_object('ok',true,'name',v_name,'time',to_char(v_time,'HH24:MI'),
    'grace', (v_time <= s_grace));
end $$;

grant execute on function do_checkout(bigint, text) to anon, authenticated;

